  async executeStep(stepName, callback) {
    const stepStartTime = performance.now();
    
    // „Éñ„É¨„Éº„ÇØ„Éù„Ç§„É≥„Éà„ÉÅ„Çß„ÉÉ„ÇØ
    if (this.breakpoints.has(stepName)) {
      this.log(`„Éñ„É¨„Éº„ÇØ„Éù„Ç§„É≥„Éà: ${stepName}`, 'WARNING');
      this.stepPaused = true;
      this.executionMode = 'step';
    }
    
    // „Çπ„ÉÜ„ÉÉ„Éó„É¢„Éº„Éâ„Åß„ÅÆ‰∏ÄÊôÇÂÅúÊ≠¢
    if (this.executionMode === 'step') {
      this.log(`„Çπ„ÉÜ„ÉÉ„ÉóÂÆüË°å: ${stepName}`, 'INFO');
      this.updateCurrentStepInfo(stepName);
      this.stepPaused = true;
      
      // ‰∏ÄÊôÇÂÅúÊ≠¢„ÇíÂæÖ„Å§
      while (this.stepPaused) {
        await new Promise(resolve => setTimeout(resolve, 100));
      }
    }
    
    // ÂÆüË°å
    try {
      await callback();
      
      const stepEndTime = performance.now();
      const duration = stepEndTime - stepStartTime;
      
      this.performanceData.push({
        step: stepName,
        duration,
        success: true
      });
      
      this.executionHistory.push({
        step: stepName,
        timestamp: new Date(),
        duration,
        success: true
      });
      
    } catch (error) {
      const stepEndTime = performance.now();
      const duration = stepEndTime - stepStartTime;
      
      this.performanceData.push({
        step: stepName,
        duration,
        success: false,
        error: error.message
      });
      
      this.executionHistory.push({
        step: stepName,
        timestamp: new Date(),
        duration,
        success: false,
        error: error.message
      });
      
      throw error;
    }
  }
  
